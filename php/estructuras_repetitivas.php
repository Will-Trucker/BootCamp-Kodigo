<?php 
    //Estructuras repetitivas
    //FOR

    for($i = 0; $i < 5; $i++){
        print("El valor de I es: {$i} \n");
    }

    //WHILE
    $contador = 1;
    while($contador < 5){
        print("El valor de CONTADOR es: {$contador} \n");
        $contador++;
    }

    //Do While

    do{
        print("El valor de CONTADOR es: {$contador} \n");
        $contador++;
    }  while($contador < 5)

?>